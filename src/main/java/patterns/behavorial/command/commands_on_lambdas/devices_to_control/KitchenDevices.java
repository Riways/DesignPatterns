package patterns.behavorial.command.commands_on_lambdas.devices_to_control;

public class KitchenDevices {
	
	public void turnOnLight() {
		System.out.println("Light in the kitchen was turned on");
	}
	
	public void turnOffLight() {
		System.out.println("Light in the kitchen was turned off");
	}
	
	public void turnOnOven() {
		System.out.println("Oven was turned on");
	}
	
	public void turnOffOven() {
		System.out.println("Oven  was turned off");
	}
	
	public void turnOnConditioner() {
		System.out.println("Conditioner in the kitchen was turned on");
	}
	
	public void turnOffConditioner() {
		System.out.println("Conditioner in the kitchen was turned off");
	}
	
	public void turnOnTV() {
		System.out.println("TV in the kitchen was turned on");
	}
	
	public void turnOffTV() {
		System.out.println("TV in the kitchen was turned off");
	}
	
	
	
}
